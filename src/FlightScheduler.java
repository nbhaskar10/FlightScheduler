    private void bookSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookSubmitButtonActionPerformed
        // TODO add your handling code here:
        flight=bookFlightComboBox.getSelectedItem().toString();
        date=Date.valueOf(dateComboBox.getSelectedItem().toString());
        customer=customerNameTextField.getText();
        timestamp=new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        if (Flights.getSeatCount(flight)>Bookings.getBookedSeatsCount(flight, date)){
            System.out.println(flight+", "+date);
            Bookings.insertBookings(customer, flight, date, timestamp);
            bookCustomerTextField.setText(customer+" was booked on flight "+flight+" on "+date);
        }
        else
        {
            
            Waitlist.insertWaitlist(customer, flight, date, timestamp);
            bookCustomerTextField.setText(customer+" was placed on the waitlist for flight "+flight+" on "+date);
        }
        statusCustomerComboBox.setModel(new javax.swing.DefaultComboBoxModel(Bookings.getallNames().toArray()));
        cancelCustomerComboBox.setModel(new javax.swing.DefaultComboBoxModel(Bookings.getallNames().toArray()));
    }//GEN-LAST:event_bookSubmitButtonActionPerformed

