    private void cancelSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelSubmitButtonActionPerformed
        // TODO add your handling code here:
        customer=cancelCustomerComboBox.getSelectedItem().toString();
        date=Date.valueOf(cancelDateComboBox.getSelectedItem().toString());
        ScheduleEntry entry=Bookings.isBooked(customer,date);
        if (Bookings.isBooked(customer,date)!=null){
            Bookings.deleteBooking(customer, date);
            ScheduleEntry entry2=Bookings.bookFromWaitlist(entry.getFlight(), date);  
            
            if (entry2!=null)
                cancelTextArea.setText("Deleted the booking of flight "+entry.getFlight()+ "on "+date+" for customer: "+customer+"\n and booked "+entry2.getCustomer()+" off of the waitlist");
            else
                cancelTextArea.setText("Deleted the booking of flight "+entry.getFlight()+ " on "+date+" for customer: "+customer);
        }
        else{
            Waitlist.deleteWaitlistEntry(customer, date);
            cancelTextArea.setText("Deleted " +customer+" from the waitlist "+ "on "+date);
        }
        cancelCustomerComboBox.setModel(new javax.swing.DefaultComboBoxModel(Bookings.getallNames().toArray()));
        statusCustomerComboBox.setModel(new javax.swing.DefaultComboBoxModel(Bookings.getallNames().toArray()));
    }//GEN-LAST:event_cancelSubmitButtonActionPerformed

